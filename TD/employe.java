public class employe {
    private String prenom;
    private String nom;
    double salaire;
    private double salaireHoraire;
    private int nombreDheuresParSemaine;
}

public employe(String p, String n, double s, int h) {
    prenom = p;
    nom = n;
    salaire = s;
    salaireHoraire = s;
    nombreDheuresParSemaine = h;
}